package POO;

public class Cliente {
	public int itensComprados;
	public double quantGasta;
	public String nome;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getItensComprados() {
		return itensComprados;
	}

	public void setItensComprados(int itensComprados) {
		this.itensComprados = itensComprados;
	}

	public double getQuantGasta() {
		return quantGasta;
	}

	public void setQuantGasta(double quantGasta) {
		this.quantGasta = quantGasta;
		
	}
	

}

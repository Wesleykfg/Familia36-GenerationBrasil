package POO;

public class Aviao {
	public String nome;
	public String modelo;
	public double valor;
	public String Cor;
	public int quantBancos;
	public int quantFuncionarios;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public String getCor() {
		return Cor;
	}
	public void setCor(String cor) {
		Cor = cor;
	}
	public int getQuantBancos() {
		return quantBancos;
	}
	public void setQuantBancos(int quantBancos) {
		this.quantBancos = quantBancos;
	}
	public int getQuantFuncionarios() {
		return quantFuncionarios;
	}
	public void setQuantFuncionarios(int quantFuncionarios) {
		this.quantFuncionarios = quantFuncionarios;
	}
	
	

}
